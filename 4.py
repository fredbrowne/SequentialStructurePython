# Make a program that will take 4 grades and show the grade points average.

g1 = int(input('Type the first grade: '))
g2 = int(input('Type the second grade: '))
g3 = int(input('Type the third grade: '))
g4 = int(input('Type the forth grade: '))
average = (g1+g2+g3+g4) / 4

print(f'Your Grade Points average is {average}')